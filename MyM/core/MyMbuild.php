           break;
           
         case _VIDEO:
           build('$file = ROOT_URI."/".MYM_UPLOAD_PATH."/".$this->'.$key.';');
	   build('if (file_exists(MYM_UPLOAD_REALPATH."/".$this->'.$key.')) {');up();
