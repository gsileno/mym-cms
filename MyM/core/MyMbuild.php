           build('$'.$key.'output = $this->flagarray_index("'.$key.'", $this->'.$key.');');
           break;
 
         case _LNG:
         case _EMAIL:
         case _NUMBER:
         case _ONDB:
         case _OWNERIP:
         case _PRIV:
         case _ONID:
         case _LISTID:
         case _DATE:
         case _NOW:
